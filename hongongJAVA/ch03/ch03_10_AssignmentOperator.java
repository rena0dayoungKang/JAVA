package ch03;

public class ch03_10_AssignmentOperator {
    public static void main(String[] args) {
        int result = 10;
        result += 10;
        System.out.println(result);

        result -= 5;
        System.out.println(result);

        result *= 3;
        System.out.println(result);

        result /= 5;
        System.out.println(result);

        result %= 3;
        System.out.println(result);
    }

}
